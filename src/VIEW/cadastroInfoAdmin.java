    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome, cpf,idade,rg,data,login,senha;

            nome = nomeTextField.getText();
            cpf = cpfTextField.getText();
            idade = idadeTextField.getText();
            rg = rgTextField.getText();
            data = dataTextField.getText();
            login = loginTextField.getText();
            senha = new String(senhaPasswordField.getPassword());

            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setCadastrar_nomeFun(nome);
            objusuariodto.setCadastrar_cpfFun(cpf);
            objusuariodto.setCadastrar_idadeFun(idade);
            objusuariodto.setCadastrar_rgFun(rg);
            objusuariodto.setCadastrar_dataFun(data);
            objusuariodto.setNome_usuario(nome);
            objusuariodto.setSenha_usuario(senha);
            
            UsuarioDao objusuariodao = new UsuarioDao();
            
            ResultSet repetido = objusuariodao.funRepetido(objusuariodto);
            
        try {
            if(repetido.next()){
                JOptionPane.showMessageDialog(null,"Esse Funcionario ja existe tente outro");
            
            }
            
            else{
                int rsusuariodao = objusuariodao.funcionarioInsert(objusuariodto);            
                if(rsusuariodao>=0)JOptionPane.showMessageDialog(null,"Funcionario cadastrado");
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Cadastro view "+erro);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

